import Foundation
import SwiftData

@MainActor
struct ShoppingListGenerator {

    // MARK: - Types

    private struct NeedKey: Hashable {
        let name: String
        /// UnitConverter.aggregationKey: "volume"/"weight" for measurable units,
        /// canonical unit string for count/other — enables cross-unit aggregation.
        let aggKey: String
    }

    private struct NeedValue {
        let ingredient: Ingredient
        /// Quantity in base units: tsp (volume), g (weight), raw qty (count/other).
        var baseQuantity: Double
        let dimension: UnitDimension
        /// Canonical unit of first-seen ingredient — used as fallback display for count/other.
        let firstUnit: String
        /// Original recipe unit before density normalization (e.g. "cup" even when
        /// dimension was normalized to .weight via density). Used for recipe detail display.
        let originalRecipeUnit: String
    }

    // MARK: - Public API

    static func generate(context: ModelContext, lookaheadDays: Int = 7) {
        // Clear existing auto-generated unchecked items
        let autoGenDescriptor = FetchDescriptor<ShoppingListItem>(
            predicate: #Predicate { $0.isAutoGenerated && !$0.isChecked }
        )
        if let existing = try? context.fetch(autoGenDescriptor) {
            for item in existing {
                context.delete(item)
            }
        }

        // Get upcoming meal plan entries
        let now = DateHelpers.startOfDay(Date())
        let endDate = DateHelpers.addDays(lookaheadDays, to: now)
        let entryDescriptor = FetchDescriptor<MealPlanEntry>(
            predicate: #Predicate {
                $0.date >= now && $0.date < endDate && $0.status == "planned"
            }
        )
        guard let entries = try? context.fetch(entryDescriptor) else { return }

        // Aggregate ingredient needs.
        var needed: [NeedKey: NeedValue] = [:]

        for entry in entries {
            guard let recipe = entry.recipe else { continue }
            let servingMultiplier =
                recipe.servings > 0 ? Double(entry.servings) / Double(recipe.servings) : 1.0
            for ri in recipe.recipeIngredients {
                guard let ingredient = ri.ingredient else { continue }
                let scaledQty = ri.quantity * servingMultiplier
                let density = ingredient.density
                let dim = UnitConverter.dimension(of: ri.unit)

                let effectiveAggKey: String
                let effectiveDim: UnitDimension
                let effectiveBaseQty: Double

                if let d = density, (dim == .volume || dim == .weight),
                   let grams = UnitConverter.convert(quantity: scaledQty, from: ri.unit, to: "g", density: d) {
                    // Density known — normalize to grams so volume+weight merge.
                    effectiveAggKey = "weight"
                    effectiveDim = .weight
                    effectiveBaseQty = grams
                } else {
                    effectiveAggKey = UnitConverter.aggregationKey(for: ri.unit)
                    effectiveDim = dim
                    effectiveBaseQty = UnitConverter.toBaseUnit(quantity: scaledQty, unit: ri.unit) ?? scaledQty
                }

                let key = NeedKey(name: ingredient.name, aggKey: effectiveAggKey)

                if var existing = needed[key] {
                    existing.baseQuantity += effectiveBaseQty
                    needed[key] = existing
                } else {
                    let canonical = UnitConverter.normalize(ri.unit)
                    needed[key] = NeedValue(
                        ingredient: ingredient,
                        baseQuantity: effectiveBaseQty,
                        dimension: effectiveDim,
                        firstUnit: canonical,
                        originalRecipeUnit: canonical
                    )
                }
            }
        }

        // Build inventory map keyed by (name, aggKey) in base units.
        let inventoryDescriptor = FetchDescriptor<InventoryItem>()
        let inventory = (try? context.fetch(inventoryDescriptor)) ?? []
        var inventoryMap: [NeedKey: Double] = [:]
        for item in inventory {
            guard let ingredient = item.ingredient else { continue }
            let density = ingredient.density
            let dim = UnitConverter.dimension(of: item.unit)

            let effectiveAggKey: String
            let effectiveBaseQty: Double

            if let d = density, (dim == .volume || dim == .weight),
               let grams = UnitConverter.convert(quantity: item.quantity, from: item.unit, to: "g", density: d) {
                effectiveAggKey = "weight"
                effectiveBaseQty = grams
            } else {
                effectiveAggKey = UnitConverter.aggregationKey(for: item.unit)
                effectiveBaseQty = UnitConverter.toBaseUnit(quantity: item.quantity, unit: item.unit) ?? item.quantity
            }

            let key = NeedKey(name: ingredient.name, aggKey: effectiveAggKey)
            inventoryMap[key, default: 0] += effectiveBaseQty
        }

        // Sort needs by category, then ingredient name, then unit for deterministic output.
        let sortedNeeds = needed.sorted { lhs, rhs in
            let li = lhs.value.ingredient
            let ri = rhs.value.ingredient
            let lCat = IngredientCategory.allCategories.firstIndex(of: li.category) ?? Int.max
            let rCat = IngredientCategory.allCategories.firstIndex(of: ri.category) ?? Int.max
            if lCat != rCat { return lCat < rCat }
            let nameOrder = li.displayName.localizedCaseInsensitiveCompare(ri.displayName)
            if nameOrder != .orderedSame { return nameOrder == .orderedAscending }
            let unitOrder = lhs.value.firstUnit.localizedCaseInsensitiveCompare(rhs.value.firstUnit)
            return unitOrder == .orderedAscending
        }

        // Create shopping list items for any positive remaining need.
        for (key, need) in sortedNeeds {
            var baseToBuy = need.baseQuantity
            if let baseOnHand = inventoryMap[key] {
                baseToBuy -= baseOnHand
            }
            guard baseToBuy > 0 else { continue }

            // Compute precise recipe display quantity.
            let (recipeQty, recipeUnit) = recipeDisplayQuantity(
                baseQty: baseToBuy, dimension: need.dimension,
                fallbackUnit: need.firstUnit,
                originalRecipeUnit: need.originalRecipeUnit,
                density: need.ingredient.density
            )

            // Convert base quantity to a shelf-purchasable (quantity, unit) pair.
            let (displayQty, displayUnit) = shelfDisplayQuantity(
                baseQty: baseToBuy,
                dimension: need.dimension,
                category: need.ingredient.category,
                ingredientName: need.ingredient.name,
                fallbackUnit: need.firstUnit
            )

            let item = ShoppingListItem(
                quantity: displayQty,
                unit: displayUnit,
                recipeQuantity: recipeQty,
                recipeUnit: recipeUnit,
                isAutoGenerated: true,
                ingredient: need.ingredient
            )
            context.insert(item)
        }

        try? context.save()
    }

    // MARK: - Private helpers

    /// Compute the precise recipe-calculated quantity for display in detail view.
    /// When an ingredient was density-normalized (e.g. "cup" → grams), converts back
    /// to the original recipe unit so the detail shows "6 cup" instead of "1420 g".
    private static func recipeDisplayQuantity(
        baseQty: Double,
        dimension: UnitDimension,
        fallbackUnit: String,
        originalRecipeUnit: String,
        density: Double?
    ) -> (quantity: Double, unit: String) {
        switch dimension {
        case .volume, .weight:
            // If density normalization changed the dimension (e.g. volume "cup" → weight grams),
            // convert back to the original recipe unit for a meaningful display.
            let originalDim = UnitConverter.dimension(of: originalRecipeUnit)
            if originalDim != dimension,
               let converted = UnitConverter.convert(
                   quantity: baseQty, from: dimension == .weight ? "g" : "tsp",
                   to: originalRecipeUnit, density: density
               ) {
                return (converted, originalRecipeUnit)
            }
            // Same dimension — convert back to original recipe unit if recognized.
            if let converted = UnitConverter.fromBaseUnit(baseQuantity: baseQty, toUnit: originalRecipeUnit) {
                return (converted, originalRecipeUnit)
            }
            return UnitConverter.prettyDisplay(baseQuantity: baseQty, dimension: dimension)
        case .count, .other:
            return (baseQty, fallbackUnit)
        }
    }

    /// Compute the shelf purchase quantity (snapped to real retail sizes).
    private static func shelfDisplayQuantity(
        baseQty: Double,
        dimension: UnitDimension,
        category: String,
        ingredientName: String,
        fallbackUnit: String
    ) -> (quantity: Double, unit: String) {
        switch dimension {
        case .volume, .weight:
            if let result = PurchaseUnitCatalog.purchaseQuantitySimple(
                baseQty: baseQty, dimension: dimension, category: category, ingredientName: ingredientName
            ) {
                return result
            }
            return UnitConverter.prettyDisplay(baseQuantity: baseQty, dimension: dimension)
        case .count, .other:
            return (baseQty, fallbackUnit)
        }
    }
}
